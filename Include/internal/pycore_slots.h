#ifndef _Py_PYCORE_SLOTS_H
#define _Py_PYCORE_SLOTS_H

#ifndef Py_BUILD_CORE
#  error "this header requires Py_BUILD_CORE define"
#endif

#include <stdbool.h>

typedef enum _PySlot_TYPE {
    _PySlot_TYPE_VOID,
    _PySlot_TYPE_FUNC,
    _PySlot_TYPE_PTR,
    _PySlot_TYPE_SIZE,
    _PySlot_TYPE_INT64,
    _PySlot_TYPE_UINT64,
}_PySlot_TYPE;

typedef enum _PySlot_KIND {
    _PySlot_KIND_TYPE,
    _PySlot_KIND_MOD,
    _PySlot_KIND_COMPAT,
    _PySlot_KIND_SLOT,
} _PySlot_KIND;

typedef enum _PySlot_PROBLEM_HANDLING {
    _PySlot_PROBLEM_ALLOW = 0,
    _PySlot_PROBLEM_DEPRECATED,
    _PySlot_PROBLEM_REJECT,
} _PySlot_PROBLEM_HANDLING;

/* Internal information about a slot */
typedef struct _PySlot_Info {
    const char *name; /* without the Py_ prefix */
    _PySlot_TYPE dtype;
    _PySlot_KIND kind;
    _PySlot_PROBLEM_HANDLING null_handling;
    _PySlot_PROBLEM_HANDLING duplicate_handling;
    bool is_subslots :1;
    bool is_name :1;
    union {
        struct {
            /* For type slots (_PySlot_KIND_TYPE):
             * Most slots corresponds to members in PyTypeObject & similar
             * structs.
             * Each entry has two offsets, "slot_offset" and "subslot_offset".
             * If is subslot_offset is -1, slot_offset is an offset within the
             * PyTypeObject struct.
             * Otherwise slot_offset is an offset to a pointer to a sub-slots
             * struct (such as "tp_as_number"), and subslot_offset is the
             * offset within that struct.
             *
             * If both are zero, the slot needs special handling; that is,
             * the offset mechanism isn't used.
             */
            short subslot_offset;
            short slot_offset;
        } type_info;
        struct {
            /* For compat slots (_PySlot_KIND_COMPAT):
             * these slots were renumbered; `type_id` is the ID of the new
             * type slot; `mod_id` of the module slot.
             */
            uint8_t type_id;
            uint8_t mod_id;
        } compat_info;
    };
} _PySlot_Info;

// The actual table is generated by a script.
extern _PySlot_Info _PySlot_InfoTable[];

#define _PySlot_MAX_NESTING 5

/* State for one nesting level of a slots iterator */
typedef struct _PySlotIterator_state {
    union {
        // tagged by slot_struct_kind:
        const PySlot *slot;               // with _PySlot_KIND_SLOT
        const PyType_Slot *tp_slot;       // with _PySlot_KIND_TYPE
        const PyModuleDef_Slot *mod_slot; // with _PySlot_KIND_MOD
        const void *any_slot;
    };
    _PySlot_KIND slot_struct_kind;
    bool ignoring_fallbacks :1;
} _PySlotIterator_state;

/* State for a slots iterator */
typedef struct {
    _PySlotIterator_state *state;
    _PySlotIterator_state states[_PySlot_MAX_NESTING];
    _PySlot_KIND kind;
    uint8_t recursion_level;
    unsigned int seen[_Py_slot_COUNT / sizeof(unsigned int) + 1];
    bool is_at_end :1;
    bool is_first_run :1;

    /* Output information: */

    // The slot. Always a copy; may be modified by caller of the iterator.
    PySlot current;

    // Information about the slot
    const _PySlot_Info *info;

    // Name of the object (type/module) being defined, NULL if unknown.
    // Set by _PySlotIterator_Next as soon as it sees a tp_name/mod_name slot;
    // used for internal error messages but available to the caller too.
    // This points to the slot; must be copied for longer usage.
    // The name is not reset by rewinding.
    char *name;
} _PySlotIterator;

/* Initialize an iterator using a Py_Slot array */
PyAPI_FUNC(void)
_PySlotIterator_Init(_PySlotIterator *it, const PySlot *slots,
                     _PySlot_KIND result_kind);

/* Initialize an iterator using a legacy slot array */
PyAPI_FUNC(void)
_PySlotIterator_InitLegacy(_PySlotIterator *it, const void *slots,
                           _PySlot_KIND kind);

/* Reset a *successfully exhausted* iterator to the beginning.
 * The *slots* must be the same as for the previous
 * `_PySlotIterator_InitWithKind` call.
 * (Subsequent iterations skip some validation.)
 */
PyAPI_FUNC(void) _PySlotIterator_Rewind(_PySlotIterator *it, const void *slots);

/* Iteration function.
 *
 * Return false at the end (when successfully exhausted).
 * Otherwise (even on error), fill output information in `it` and return true.
 *
 * On error, set an exception and set `it->current.sl_id` to `Py_slot_invalid`.
 */
PyAPI_FUNC(bool) _PySlotIterator_Next(_PySlotIterator *it);

/* Return 1 if given slot was "seen" by an earlier _PySlotIterator_Next call.
 * (This state is not reset by rewinding.)
 */
PyAPI_FUNC(bool) _PySlotIterator_SawSlot(_PySlotIterator *, int);

static inline bool
_PySlot_IsStatic(PySlot *slot, const _PySlot_Info *info)
{
    return (slot->sl_flags & PySlot_STATIC)
        || (info->dtype == _PySlot_TYPE_VOID)
        || (info->dtype == _PySlot_TYPE_FUNC)
        || (info->dtype == _PySlot_TYPE_SIZE)
        || (info->dtype == _PySlot_TYPE_INT64)
        || (info->dtype == _PySlot_TYPE_UINT64);
}

#endif // _Py_PYCORE_SLOTS_H
