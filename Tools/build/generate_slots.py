#!/usr/bin/python
"""Generate type/module slot files.
"""

import io
import sys
import json
import tomllib
import argparse
import functools
import contextlib
from pathlib import Path

GENERATED_BY = 'Generated by Tools/build/generate_slots.py'

REPO_ROOT = Path(__file__).parent.parent.parent
DEFAULT_INPUT_PATH = REPO_ROOT / 'Python/slots.toml'
DEFAULT_HEADER_PATH = REPO_ROOT / 'Include/slots_generated.h'
DEFAULT_C_PATH = REPO_ROOT / 'Python/slots_generated.c'

def parse_slots(file):
    toml_contents = tomllib.load(file)
    result = [None] * len(toml_contents)
    for key, value in toml_contents.items():
        slot_id = int(key)
        try:
            if result[slot_id]:
                raise ValueError(f'slot ID {slot_id} repeated')
            kind = value['kind']
            result[slot_id] = {'id': slot_id, **value}
        except Exception as e:
            e.add_note(f'handling slot {slot_id}')
            raise
    return result


def write_header(f, slots):
    out = functools.partial(print, file=f)
    out(f'/* {GENERATED_BY} */')
    out()
    out(f'#ifndef _PY_HAVE_SLOTS_GENERATED_H')
    out(f'#define _PY_HAVE_SLOTS_GENERATED_H')
    out()
    out(f'#if !defined(Py_LIMITED_API) || Py_LIMITED_API+0 >= _Py_PACK_VERSION(3, 15)')
    out(f'#define _Py_SLOT_COMPAT_VALUE(OLD, NEW) NEW')
    out(f'#else')
    out(f'#define _Py_SLOT_COMPAT_VALUE(OLD, NEW) OLD')
    out(f'#endif')
    out()
    compat_ids = {new_name: slot['id']
                  for slot in slots
                  for new_name in slot.get('equivalents', {}).values()
                  }
    print(compat_ids)
    for slot in slots:
        if slot['kind'] == 'compat':
            continue
        slot_id = slot["id"]
        name = slot["name"]
        if compat := compat_ids.get(name):
            slot_id = f'_Py_SLOT_COMPAT_VALUE({compat}, {slot_id})'
        out(f'#define {name} {slot_id}')
    out()
    out(f'#define _Py_slot_COUNT {len(slots)}')
    out(f'#endif /* _PY_HAVE_SLOTS_GENERATED_H */')


def write_c(f, slots):
    out = functools.partial(print, file=f)
    out(f'/* {GENERATED_BY} */')
    out()
    out('#include "Python.h"')
    out('#include "pycore_slots.h"   // _PySlot_Info')
    out('#include <stddef.h>         // offsetof')
    out('#include <stdbool.h>        // true')
    out()
    out(f'_PySlot_Info _PySlot_InfoTable[_Py_slot_COUNT + 1] = {{')

    assert len(slots) == max(slot.id for slot in slots) + 1
    for slot in slots:
        try:
            out(f"    [{slot.id}] = {{")
            initializers = {
                "name": f'"Py_{slot.name}"',
                "dtype": f'_PySlot_TYPE_{slot.dtype.upper()}',
                "kind": f'_PySlot_KIND_{slot.kind.upper()}',
            }
            match slot.kind:
                case 'slot':
                    pass
                case 'compat':
                    for newslot in slots:
                        if newslot.get_int('compat') == slot.id:
                            key = f'compat_info.{newslot.kind}_id'
                            initializers[key] = newslot.id
                case 'type':
                    field = slot.get('field', slot.name)
                    if not slot.get_bool('virtual'):
                        if slot.id > FIRST_3_15_SLOT:
                            raise ValueError('new slots should be virtual')
                        tpo = 'PyTypeObject'
                        typeobj, subtable, tabletype = {
                            'tp': (tpo, None, None),
                            'mp': (tpo, 'tp_as_mapping', 'PyMappingMethods'),
                            'nb': (tpo, 'tp_as_number', 'PyNumberMethods'),
                            'sq': (tpo, 'tp_as_sequence', 'PySequenceMethods'),
                            'am': (tpo, 'tp_as_async', 'PyAsyncMethods'),
                            'bf': (tpo, 'tp_as_buffer', 'PyBufferProcs'),
                            'ht': ('PyHeapTypeObject', None, None),
                        }[slot.type_table]
                        if subtable:
                            initializers['type_info.slot_offset'] = (
                                f'offsetof({typeobj}, {subtable})')
                            initializers['type_info.subslot_offset'] = (
                                f'offsetof({tabletype}, {field})')
                        else:
                            initializers['type_info.slot_offset'] = (
                                f'offsetof({typeobj}, {field})')
                            initializers['type_info.subslot_offset'] = '-1'
                case 'mod':
                    pass
                case _:
                    raise ValueError(slot.kind)
            if slot.get_bool('subslots'):
                initializers['is_subslots'] = 'true'
            match slot.get('null'):
                case 'reject':
                    initializers['null_handling'] = '_PySlot_PROBLEM_REJECT'
                case 'allow':
                    initializers['null_handling'] = '_PySlot_PROBLEM_ALLOW'
                case None:
                    if slot.dtype in {'func', 'ptr'}:
                        if slot.id > FIRST_3_15_SLOT:
                            raise ValueError(
                                f'slot {slot.name} needs explicit NULL '
                                + 'handling specification')
                        initializers['null_handling'] = (
                            '_PySlot_PROBLEM_DEPRECATED')
            match slot.get('duplicates'):
                case 'no':
                    initializers['duplicate_handling'] = '_PySlot_PROBLEM_REJECT'
                case 'yes':
                    pass
                case None:
                    initializers['duplicate_handling'] = '_PySlot_PROBLEM_DEPRECATED'
                case _:
                    raise ValueError(slot['duplicates'])
            if slot.get_bool('is_name'):
                initializers['is_name'] = 'true'
            for name, initializer in initializers.items():
                out(f'        .{name} = {initializer},')
            out(f"    }},")
        except Exception as e:
            e.add_note(f'handling slot {slot}')
            raise
    out(f"    [{len(slots)}] = {{0}}")
    out(f"}};")



@contextlib.contextmanager
def replace_file(filename):
    file_path = Path(filename)
    with io.StringIO() as sio:
        yield sio
        try:
            old_text = file_path.read_text()
        except FileNotFoundError:
            old_text = None
        new_text = sio.getvalue()
        if old_text == new_text:
            print(f'{filename}: not modified', file=sys.stderr)
        else:
            print(f'{filename}: writing new content', file=sys.stderr)
            file_path.write_text(new_text)


def main(argv):
    if len(argv) == 1:
        # No sens calling this with no arguments.
        argv.append('--help')

    parser = argparse.ArgumentParser(prog=argv[0], description=__doc__)
    parser.add_argument(
        '-i', '--input', default=DEFAULT_INPUT_PATH,
        help=f'the input file (default: {DEFAULT_INPUT_PATH})')
    parser.add_argument(
        '--generate-all', action=argparse.BooleanOptionalAction,
        help='write all output files to their default locations')
    parser.add_argument(
        '-j', '--jsonl', action=argparse.BooleanOptionalAction,
        help='write info to stdout in "JSON Lines" format (one JSON per line)')
    outfile_group = parser.add_argument_group(
        'output files',
        description='By default, no files are generated. Use --generate-all '
            + 'or the options below to generate them.')
    outfile_group.add_argument(
        '-H', '--header',
        help='file into which to write the public header')
    outfile_group.add_argument(
        '-C', '--cfile',
        help='file into which to write internal C code')
    args = parser.parse_args(argv[1:])

    if args.generate_all:
        if args.header is None:
            args.header = DEFAULT_HEADER_PATH
        if args.cfile is None:
            args.cfile = DEFAULT_C_PATH

    with open(args.input, 'rb') as f:
        slots = parse_slots(f)

    if args.jsonl:
        for slot in slots:
            print(json.dumps(slot.to_dict()))

    if args.header:
        with replace_file(args.header) as f:
            write_header(f, slots)

    if args.cfile:
        with replace_file(args.cfile) as f:
            write_c(f, slots)

if __name__ == "__main__":
    main(sys.argv)
