"""transpile.py — Python → POSIX AWK transpiler for configure.py.

Parses configure.py and all conf_*.py files, then generates a self-contained
POSIX AWK script that is functionally equivalent to running configure.py
with Python.

Usage:
    python transpile.py [-o OUTPUT]
"""

from __future__ import annotations

import os
import sys

from . import py_to_pysh
from .pysh_ast import ModuleInfo, OptionInfo
from .py_to_pysh import parse_option_decl
from .lint_transpilable import lint_file

from . import pysh_to_awk
from . import awk_emit

__all__ = [
    "ModuleInfo",
    "OptionInfo",
    "parse_option_decl",
    "transpile_configure",
]


_SHELL_WRAPPER = """\
#!/bin/sh
# Generated by transpile.py -- do not edit.
# This is a POSIX shell wrapper around an embedded AWK program.
# Run: /bin/sh configure [options]

# _find_awk — pick a working AWK that is not gawk.
# gawk 5.2.x (Ubuntu 24.04) has a crash bug ("double free or corruption")
# triggered by heavy system() usage in large scripts.  mawk and one-true-awk
# (nawk / BWK awk) are unaffected and faster, so prefer them.
_find_awk() {
    for _c in mawk nawk awk; do
        # Locate $_c in PATH without relying on "command -v".
        IFS=:
        for _d in $PATH; do
            if [ -x "${_d}/${_c}" ]; then
                # Reject gawk — it sometimes masquerades as "awk" or "nawk".
                case $("${_d}/${_c}" --version < /dev/null 2>&1) in
                    *"GNU Awk"*) ;;
                    *) echo "${_d}/${_c}"; return 0 ;;
                esac
            fi
        done
        unset IFS
    done
    # Last resort: bare "awk" (may be gawk, but better than nothing).
    echo awk
}
_awk=$(_find_awk)

_tmpf=$(mktemp "${TMPDIR:-/tmp}/configure.XXXXXXXXXX") || exit 1
trap 'rm -f "$_tmpf"' EXIT
sed '1,/^#---AWK-START---$/d' "$0" > "$_tmpf"
exec "$_awk" -f "$_tmpf" -- "$@"
#---AWK-START---
"""


def transpile_configure(script_dir: str) -> str:
    # Lint conf_*.py files before transpiling
    import glob as _glob

    conf_files = sorted(_glob.glob(os.path.join(script_dir, "conf_*.py")))
    all_violations = []
    for filepath in conf_files:
        result = lint_file(filepath)
        all_violations.extend(result.violations)
    if all_violations:
        msgs = "\n".join(f"  {v}" for v in all_violations)
        raise ValueError(
            f"lint check failed with {len(all_violations)} violation(s):\n{msgs}"
        )

    # Parse Python source, produce Python AST, transform to pysh AST
    program = py_to_pysh.parse_program(script_dir)
    # Transform pysh AST → AWK AST
    awk_program = pysh_to_awk.transform_program(program)
    # Generate AWK code, wrap in shell script
    awk_code = awk_emit.generate_code(awk_program)
    return _SHELL_WRAPPER + awk_code


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def main():
    import argparse

    parser = argparse.ArgumentParser(
        description="Transpile configure.py to POSIX AWK"
    )
    parser.add_argument(
        "-o", "--output", default="-", help="output file (default: stdout)"
    )
    parser.add_argument(
        "--dir",
        default=None,
        help="directory containing configure.py and conf_*.py",
    )
    args = parser.parse_args()

    script_dir = args.dir or os.path.dirname(
        os.path.dirname(os.path.abspath(__file__))
    )

    try:
        output = transpile_configure(script_dir)
    except Exception as e:
        print(f"transpile error: {e}", file=sys.stderr)
        import traceback

        traceback.print_exc()
        sys.exit(1)

    if args.output == "-":
        sys.stdout.write(output)
    else:
        with open(args.output, "w") as f:
            f.write(output)
        os.chmod(args.output, 0o755)
        print(f"wrote {args.output}", file=sys.stderr)


if __name__ == "__main__":
    main()
